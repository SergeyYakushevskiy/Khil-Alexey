S = 'Example'
print('Строка:', S)
print('Длина строки:', len(S))
print('3-ий символ строки:', S[2])
print('Последний символ строки:', S[-1])
print('Символы строки со второго по 6ой не включительно:', S[1:5])
print('Все символы строки, начиная с 3го:', S[2:])
print('Слияение строк: ', S + 'what')
print('Повторение строки 3 раза:', S * 3)
print('Поиск по шаблону "am" (результат - индекс первого совпадения):', S.find('am'))
print('Замена подстроки "am" на подстроку "QWE": ', S.replace('am', 'QWE'))